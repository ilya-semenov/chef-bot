async def get_cooking_advice(user_query: str, user_name: str = None) -> str:
    """
    Получение совета по приготовлению от DeepSeek API
    """
    # Если нет API ключа, возвращаем профессиональный тестовый ответ
    if not DEEPSEEK_API_KEY or DEEPSEEK_API_KEY == "ваш_api_ключ_deepseek_сюда":
        return get_professional_mock_response(user_query, user_name)
    
    headers = {
        "Authorization": f"Bearer {DEEPSEEK_API_KEY}",
        "Content-Type": "application/json"
    }
    
    # Формируем запрос
    if user_name:
        base_message = f"Меня зовут {user_name}. "
    else:
        base_message = ""
    
    if ',' in user_query:
        parts = user_query.split(',', 1)
        dish = parts[0].strip()
        goal = parts[1].strip()
        user_message = f"{base_message}Научи меня готовить {dish}. Моя цель: {goal}."
    else:
        user_message = f"{base_message}Научи меня готовить {user_query}."
    
    payload = {
        "model": DEEPSEEK_MODEL,
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_message}
        ],
        "max_tokens": MAX_TOKENS,
        "temperature": TEMPERATURE
    }
    
    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(DEEPSEEK_API_URL, headers=headers, json=payload, timeout=30) as response:
                if response.status == 200:
                    result = await response.json()
                    return result["choices"][0]["message"]["content"]
                else:
                    print(f"Ошибка API DeepSeek: {response.status}")
                    return get_professional_mock_response(user_query, user_name)
    except Exception as e:
        print(f"Ошибка при запросе к DeepSeek API: {e}")
        return get_professional_mock_response(user_query, user_name)

